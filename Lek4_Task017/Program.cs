void PrintImage(int[,] image) // метод вывода двумерного массива 
{
    for (int i = 0; i < image.GetLength(0); i++) // 0 - строки
    {
        for (int j = 0; j < image.GetLength(1); j++) // 1 - столбцы
        {
            //Console.Write($"{image[i, j]} ");
            if (image[i,j] == 0) Console.Write($" ");
            else Console.Write($"+");
        }
        Console.WriteLine();
    }
}

void FillImage(int row, int col) // алгоритм, ктр закрашивает замкнутые области
{
    if(pic[row, col] == 0)
    {
        pic[row, col] = 1;
        FillImage(row - 1, col);
        FillImage(row, col - 1);
        FillImage(row + 1, col);
        FillImage(row, col + 1);

    }
}
